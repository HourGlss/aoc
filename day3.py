def deliver(_x, _y, _houses):
    temp = House(_x, _y)
    if temp.uid not in _houses.keys():
        _houses[temp.uid] = temp
    _houses[temp.uid].add_present()


def part1(_houses: dict, _input):
    x = 0
    y = 0
    deliver(x, y, _houses)
    # print(x, y,_houses)
    for c in _input:

        if c == "v":
            y += 1
        if c == "^":
            y -= 1
        if c == ">":
            x += 1
        if c == "<":
            x -= 1
        deliver(x, y, _houses)
    return len(_houses.keys())


def part2(_input):
    a = ""
    b = ""
    houses = {}
    for _, c in enumerate(_input):
        _ += 1
        if _ % 2 == 0:
            a += c
        else:
            b += c
    part1(houses, a)
    part1(houses, b)
    return len(houses.keys())


