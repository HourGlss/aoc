import hashlib


class House:
    x: int
    y: int
    num_presents: int
    uid: str

    def __init__(self, _x, _y):
        self.x = _x
        self.y = _y
        self.num_presents = 0
        _ = hashlib.sha256()
        _.update(f"{self.x}.{self.y}".encode())
        self.uid = _.hexdigest()[:8]

    def add_present(self):
        self.num_presents += 1

    def __repr__(self):
        return str(self)

    def __str__(self):
        return f"({self.x},{self.y})"


